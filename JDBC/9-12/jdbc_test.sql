

-- 회원 정보 테이블

CREATE TABLE TBL_MEMBER (
    MEMBER_ID VARCHAR2(20) PRIMARY KEY, -- 회원 ID
    MEMBER_PW VARCHAR2(20) NOT NULL,    -- 회원 비밀번호
    MEMBER_NAME VARCHAR2(20) NOT NULL,  -- 회원 이름
    MEMBER_PHONE CHAR(13) NOT NULL,     -- 전화번호
    MEMBER_AGE NUMBER NOT NULL,         -- 나이
    MEMBER_GENDER CHAR(3) CHECK(MEMBER_GENDER IN ('남', '여')), -- 성별
    ENROLL_DATE DATE -- 가입일
);


